    def run_silver(self,data):
        locks,keys = [], []
        for lk in data.split("\n\n"):
            (locks if "#" in lk[0] else keys).append([barrel.count("#") for barrel in zip(*lk.splitlines())])

        return sum(all(ln + kn <= 7 for ln,kn in zip(l,k)) for l,k in product(locks,keys))

    def run_gold(self,data):
        return "Happy Christmas"

