from itertools import product
from collections import defaultdict
from sys import argv
from aoc import AocDay

class AocDay12(AocDay):

    def get_regions(self,data):
        W,S,E,N = (-1, 0), (0, 1), (1, 0), (0, -1)
        grid = data.splitlines()
        xdim,ydim = len(grid[0]),len(grid)
        plot = {(x,y):grid[y][x] for x,y in product(range(xdim),range(ydim))}
        visited = set()
        for start in plot:
            if start in visited: continue
            plant = plot[start]
            todo = [start]
            region = set()
            while todo:
                pos = todo.pop()
                if pos in region: continue
                region.add(pos)
                for dir in (W,S,E,N):
                    nxt = (pos[0]+dir[0],pos[1]+dir[1])
                    if plot.get(nxt,"") == plant:
                        todo.append(nxt)
            visited.update(region)
            yield region
    
    def perimeter(region):
        W,S,E,N = (-1, 0), (0, 1), (1, 0), (0, -1)
        dirs = (W,S,E,N)
        perimeter = 0
        for pos in region:
            for dir in dirs:
                nxt = (pos[0]+dir[0],pos[1]+dir[1])
                if nxt not in region:
                    perimeter += 1
        return perimeter

    def sides(region):
        W,S,E,N = (-1, 0), (0, 1), (1, 0), (0, -1)
        dirs = (W,S,E,N)
        perimeter = defaultdict(set)
        for pos in region:
            for dir in dirs:
                nxt = (pos[0]+dir[0],pos[1]+dir[1])
                if nxt not in region:
                    perimeter[dir].add(pos)
        
        sides = 0
        for space,edgecells in perimeter.items():
            on_edge = set()
            look = (W,E) if space in (N,S) else (N,S)
            for pos in edgecells:
                if pos in on_edge: continue
                on_edge.add(pos)
                # Consume cells on the edge that are perpendicular to the space
                for dir in look:
                    while (nxt := (pos[0]+dir[0],pos[1]+dir[1])) in edgecells:
                        pos = nxt
                        on_edge.add(pos)
                sides += 1
        return sides

    def run_silver(self,data):
        return sum(AocDay12.perimeter(region) * len(region) for region in self.get_regions(data))
        
    def run_gold(self,data):
        return sum(AocDay12.sides(region) * len(region) for region in self.get_regions(data))

