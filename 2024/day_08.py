#!/usr/bin/env python3 
from itertools import combinations, product
from collections import defaultdict
from sys import argv
from aoc import AocDay

class AocDay8(AocDay):

    def parser(self,data):
        grid = data.splitlines()
        xdim, ydim = len(grid[0]),len(grid)
        antennas, city = defaultdict(list), set()
        for pos in product(range(xdim),range(ydim)):
            city.add(pos)
            if (freq := grid[pos[1]][pos[0]]) != ".":
                antennas[freq].append(pos)
        return city,antennas
    
    def extrapolate(a,b,ab,city,dist):
        return set(((b[0] + dist * ab[0], b[1] + dist * ab[1]),
                    (a[0] - dist * ab[0], a[1] - dist * ab[1]))) & city

    def delta(a,b):
        return (b[0] - a[0], b[1] - a[1])
    
    def run_silver(self,data):
        city,antennas = self.parser(data)
        antinodes = set(n for pos in antennas.values()
                        for a,b in combinations(pos, 2)
                        for n in AocDay8.extrapolate(a,b,AocDay8.delta(a,b),city,1))
        return len(antinodes)

    def run_gold(self,data):
        city,antennas = self.parser(data)
        antinodes = set()
        for params in ((a,b,AocDay8.delta(a,b)) for pos in antennas.values() for a,b in combinations(pos, 2)):
            dist = 0
            while new := AocDay8.extrapolate(*params,city,dist):
                antinodes.update(new)
                dist += 1
        return len(antinodes)

