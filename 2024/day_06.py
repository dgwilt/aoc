#!/usr/bin/env python3 
from itertools import product
from sys import argv
from aoc import AocDay

class AocDay6(AocDay):

    N,S,W,E = (0,-1),(0,1),(-1,0),(1,0)
    TURNS = {N:E,E:S,S:W,W:N}

    def parser(self,data):
        grid = data.splitlines()
        xdim,ydim = len(grid[0]),len(grid)
        plan, obstacles = set(), set()
        for pos in product(range(xdim),range(ydim)):
            plan.add(pos)
            cell = grid[pos[1]][pos[0]]
            if cell == "^": guard = pos
            elif cell == "#": obstacles.add(pos)
        return plan,guard,obstacles

    def move(self,guard,dir,obstacles):
        while (nxt := (guard[0]+dir[0],guard[1]+dir[1])) in obstacles:
            dir = AocDay6.TURNS[dir]
        return nxt,dir

    def get_route(self,plan,guard,obstacles):
        dir,visited = AocDay6.N, set()
        while guard in plan:
            visited.add(guard)
            guard,dir = self.move(guard,dir,obstacles)
        return visited
    
    def run_silver(self,data):
        plan,guard,obstacles = self.parser(data)
        return len(self.get_route(plan,guard,obstacles))
            
    def run_gold(self,data):
        plan,start,obstacles = self.parser(data)
        answer = 0
        for obstruction in self.get_route(plan,start,obstacles) - set([start]):
            obstacles.add(obstruction)

            guard,dir,visited = start, AocDay6.N, set()
            while guard in plan and (noloop := ((guard,dir) not in visited)):
                visited.add((guard,dir))
                guard,dir = self.move(guard,dir,obstacles)

            answer += (not noloop)
            obstacles.remove(obstruction)
        return answer
    
