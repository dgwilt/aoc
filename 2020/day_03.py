#!/usr/bin/env python3 
from sys import argv
from aoc import AocDay, prod

class AocDay3(AocDay):

    def setup(self,data):
        self.grid = data.splitlines()
        self.width = len(self.grid[0])
        self.height = len(self.grid)

    def toboggan_run(self,dx,dy):
        return sum(self.grid[y][(t*dx) % self.width] == "#" for t,y in enumerate(range(0,self.height,dy)))

    def run_silver(self,data):
        self.setup(data)
        return self.toboggan_run(3,1)

    def run_gold(self,data):
        self.setup(data)
        return prod(self.toboggan_run(*slope) for slope in ((1,1),(3,1),(5,1),(7,1),(1,2)))

