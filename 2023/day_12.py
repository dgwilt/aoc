#!/usr/bin/env python3 
from sys import argv
from aoc import AocDay
from functools import lru_cache

class AocDay12(AocDay):

    @lru_cache
    def arrangements(condition, springs):
        if not springs:
            return 0 if "#" in condition else 1
        current, springs, result = springs[0], springs[1:], 0
        for i in range(len(condition) - sum(springs) - len(springs) - current + 1):
            if "#" in condition[:i]:
                break
            nxt = i + current
            # Slicing a string out of range returns an empty string rather than an error.
            if nxt <= len(condition) and '.' not in condition[i : nxt] and condition[nxt : nxt + 1] != "#":
                result += AocDay12.arrangements(condition[nxt + 1:], springs)
        return result

    def parser(self,data):
        for line in data.splitlines():
            condition, springs = line.split()
            # Tuple needed for lru_cache
            yield condition,tuple([int(i) for i in springs.split(",")])

    def run_silver(self,data):
        return sum(AocDay12.arrangements(condition,springs) for condition,springs in self.parser(data))

    def run_gold(self,data):
        return sum(AocDay12.arrangements("?".join([condition] * 5),springs * 5) for condition,springs in self.parser(data))

