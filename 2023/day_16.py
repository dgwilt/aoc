#!/usr/bin/env python3 
from sys import argv
from aoc import AocDay

N,S,W,E = (0,-1),(0,1),(-1,0),(1,0)

class AocDay16(AocDay):

    def run(grid,xdim,ydim,start):
        mirrors = {
            "\\": {E:[S], N:[W], W:[N],   S:[E]},
            "/" : {N:[E], W:[S], S:[W],   E:[N]},
            "|" : {N:[N], S:[S], E:[N,S], W:[N,S]}, # Splitter
            "-" : {W:[W], E:[E], N:[W,E], S:[W,E]}, # Splitter
            "." : {N:[N], S:[S], W:[W],   E:[E]}
        }
        beams = set()
        todo = [start]
        while todo:
            beam = todo.pop()
            if beam in beams: continue
            beams.add(beam)

            (x0,y0),dir = beam
            for nxtdir in mirrors[grid[y0][x0]][dir]:
                dx,dy = nxtdir
                x = x0 + dx
                y = y0 + dy
                if 0 <= x < xdim and 0 <= y < ydim:
                    todo.append(((x,y),nxtdir))

        return len(set([p for p,_ in beams]))

    def parser(self,data):
        grid = data.splitlines()
        xdim = len(grid[0])
        ydim = len(grid)
        return grid,xdim,ydim
    
    def run_silver(self,data):
        grid,xdim,ydim = self.parser(data)
        return AocDay16.run(grid,xdim,ydim,((0,0),E))
                        
    def run_gold(self,data):
        grid,xdim,ydim = self.parser(data)
        starts = ((E,((0,y) for y in range(ydim))), 
                  (W,((xdim-1,y) for y in range(ydim))), 
                  (S,((x,0) for x in range(xdim))), 
                  (N,((x,ydim-1) for x in range(xdim))))
        return max(AocDay16.run(grid,xdim,ydim,(edge,dir)) for dir,edges in starts for edge in edges)
    
