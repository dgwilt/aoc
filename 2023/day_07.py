from aoc import AocDay

class AocDay7(AocDay):

    FIVEOFAKIND = 7
    FOUROFAKIND = 6
    FULLHOUSE = 5
    THREEOFAKIND = 4
    TWOPAIRS = 3
    ONEPAIR = 2
    HIGHCARD = 1
    J_AS_JACK = 11
    J_AS_JOKER = 1

    def eval_hand(h):
        num_different_cards = len(h)
        max_of_one_card = max(h.values())
        match num_different_cards:
            case 1: return AocDay7.FIVEOFAKIND
            case 2: return AocDay7.FOUROFAKIND if max_of_one_card == 4 else AocDay7.FULLHOUSE
            case 3: return AocDay7.THREEOFAKIND if max_of_one_card == 3 else AocDay7.TWOPAIRS
            case 4: return AocDay7.ONEPAIR
            case other: return AocDay7.HIGHCARD

    def rank_silver(cards):
        return AocDay7.eval_hand(Counter(cards))

    def rank_gold(cards):
        hand = Counter(cards)
        if (jokers := hand[AocDay7.J_AS_JOKER]) == 5:
            return AocDay7.FIVEOFAKIND
        
        del hand[AocDay7.J_AS_JOKER] # Remove jokers
        for card in hand:
            hand[card] += jokers
        return AocDay7.eval_hand(hand)

    def parser(self,data,j_value):
        card_values = {"T":10,"J":j_value,"Q":12,"K":13,"A":14,"9":9,"8":8,"7":7,"6":6,"5":5,"4":4,"3":3,"2":2}
        for line in data.splitlines():
            hand,bet = line.split()
            hand = [card_values[c] for c in hand]
            yield hand,bet

    def run(self,data,rank,j_value):
        hands = sorted([[rank(hand),hand,int(bet)] for hand,bet in self.parser(data,j_value)])
        return sum(rank * bet for rank,(_,_,bet) in enumerate(hands,start=1))

    def run_silver(self,data):
        return self.run(data,AocDay7.rank_silver,AocDay7.J_AS_JACK)
            
    def run_gold(self,data):
        return self.run(data,AocDay7.rank_gold,AocDay7.J_AS_JOKER)

