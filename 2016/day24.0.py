xdim = len(lines[0])
mymap = [[None]*xdim for _ in range(ydim)]
locations = {}

# Build data structure of input data
for y,l in enumerate(lines):
	for x,c in enumerate(list(l)):
		mymap[y][x] = False if c == "#" else True
		if c.isdigit():
			locations[(x,y)] = int(c)

def update(x,y,here,pathlen,locations,movegrid,graph):
	movegrid[y][x] = pathlen
	if (x,y) in locations:
		to = locations[(x,y)]
		graph[here][to] = pathlen
		graph[to][here] = pathlen

# Build graph of the shortest routes from each location to each other location using
# a flood-fill approach
graph = {i:{} for i in locations.values()}
for pos,fr in locations.items():
	movegrid = [[None]*xdim for _ in range(ydim)]
	movegrid[pos[1]][pos[0]] = 0
	done = [[False]*xdim for _ in range(ydim)]
	while len(graph[fr]) < len(locations) - 1:
		for y in range(ydim):
			for x in [i for i in range(xdim) if mymap[y][i] and movegrid[y][i] is not None and not done[y][i]]:
				pathlen = movegrid[y][x] + 1
				done[y][x] = True
				for xx in [i for i in range(max(0,x-1),min(xdim,x+2)) if i != x and mymap[y][i] and movegrid[y][i] is None]:
					update(xx,y,fr,pathlen,locations,movegrid,graph)
				for yy in [i for i in range(max(0,y-1),min(ydim,y+2)) if i != y and mymap[i][x] and movegrid[i][x] is None]:
					update(x,yy,fr,pathlen,locations,movegrid,graph)

def get_next_states(state):
	(graph,here,pathlen) = state
	next_states = []
	for to in graph[here]:
		next_state = ({k1:{k2:v2 for k2,v2 in v1.items() if k2 != here} for k1,v1 in graph.items() if k1 != here},to,pathlen + graph[here][to])
		next_states.append(next_state)
	return next_states

def run1(graph):
	# Now run a breadth-first search of the graph, starting at zero
	# States are the graph, where I am and the current path length
	states = [(graph,0,0)]
	while True:
		next_states = []
		for state in states:
			next_states.extend(get_next_states(state))
		if len(next_states) == 0:
			shortest = min(states,key=lambda x:x[2])
			return(shortest[2])
		states = next_states

print(run1(graph))

