from aoc import AocDay
import turtle

dir = lambda x: 0 if x == 0 else 1 if x > 0 else -1
   
class AocDay9(AocDay):

    VISUALIZE = False
    
    def visualize(self,rope):
        head = rope[0]
        tail = rope[-1]
        self.headpen.setposition(5*head[0]+200,5*head[1]-300)
        self.tailpen.setposition(5*tail[0]+200,5*tail[1]-300)
        
    def turtle_setup(self):
        turtle.Screen().screensize(900, 900)
        self.headpen = turtle.Turtle()
        self.headpen.width(3)
        self.headpen.color("red")
        self.headpen.hideturtle()
        self.headpen.speed("fastest")
        self.headpen.penup()
        self.headpen.setposition(200,-300)
        self.headpen.pendown()
        self.tailpen = turtle.Turtle()
        self.tailpen.width(3)
        self.tailpen.color("blue")
        self.tailpen.hideturtle()
        self.tailpen.speed("fastest")
        self.tailpen.penup()
        self.tailpen.setposition(200,-300)
        self.tailpen.pendown()


    def move_forwards(self,head,tail):
        hc,hr = head
        tc,tr = tail
        cdelta = hc - tc
        rdelta = hr - tr
        return tail if abs(cdelta) <= 1 and abs(rdelta) <= 1 else (tc + dir(cdelta),tr + dir(rdelta))

    def run_rope(self,data,knots):
        rope = [(0,0) for _ in range(knots)]
        visits = set([rope[-1]])
        if AocDay9.VISUALIZE: self.turtle_setup()
        for instruction in data.splitlines():
            dir,steps = instruction.split()
            cdelta,rdelta = self.udlr[dir]
            for _ in range(int(steps)):
                hc,hr = rope[0]
                rope[0] = (hc + cdelta,hr + rdelta)
                for i in range(1,knots):
                    rope[i] = self.move_forwards(rope[i-1],rope[i])
                if AocDay9.VISUALIZE: self.visualize(rope)
                visits.add(rope[-1])

        return len(visits)

    def run_silver(self,data):
        return self.run_rope(data,2)

    def run_gold(self,data):
        return self.run_rope(data,10)

