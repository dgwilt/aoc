from itertools import product
from sys import argv
from aoc import AocDay

class AocDay20(AocDay):

    def enhance_image(data,steps):
        translations, grid = data.split("\n\n")
        tr = {ord(fr):ord(to) for fr,to in zip('.#','01')} # To allow an int-cast

        grid = [row for row in grid.splitlines()]
        xmn,ymn = 0,0
        xmx,ymx = len(grid[0]),len(grid)
        grid = {(x,y):grid[y][x] for x,y in product(range(xmx),range(ymx))}

        for step in range(steps):
            default = "." if translations[0] == "." else "#" if step % 2 else "." # Sneaky # at position 0 and . at final position makes infinite grid 'flash'
            nextgrid = {}
            for x,y in product(range(xmn-1,xmx+2),range(ymn-1,ymx+2)):
                bin = "".join(grid.get((col,row),default) for row in range(y-1,y+2) for col in range(x-1,x+2))
                nextgrid[(x,y)] = translations[int(bin.translate(tr),2)]
            grid = nextgrid
            xmn, xmx, ymn, ymx = xmn-1,xmx+1,ymn-1,ymx+1

        return list(grid.values()).count("#")

    def run_silver(self,data):
        return AocDay20.enhance_image(data,2)
        
    def run_gold(self,data):
        return AocDay20.enhance_image(data,50)

