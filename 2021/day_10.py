class AocDay10(AocDay):

    def run_silver(self,data):
        score = 0
        values = {")":3,"]":57,"}":1197,">":25137}
        for line in data.splitlines():
            stack = []
            for letter in line:
                if letter in "([{<":
                    stack.append(letter)
                elif stack.pop()+letter not in "()[]{}<>":
                    score += values[letter]
                    break
        return score

    def run_gold(self,data):
        scores = []
        values = {"(":1,"[":2,"{":3,"<":4}
        for line in data.splitlines():
            stack = []
            for letter in line:
                if letter in "([{<":
                    stack.append(letter)
                elif stack.pop()+letter not in "()[]{}<>":
                    break
            else:
                scores.append(reduce(lambda s,l: 5*s + values[l],stack[::-1],0))
        scores.sort()
        return scores[len(scores)//2]

