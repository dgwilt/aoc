entrance = "@"
wall = "#"
L = (-1,0)
R = (1,0)
U = (0,-1)
D = (0,1)
deltas = [L,R,U,D]

def at(grid,pos):
    x,y = pos
    return grid[y][x]

def reachable_keys(start, keys, grid):
    todo = deque([(start, 0)])
    seen = set()
    while todo:
        pos, dist = todo.popleft()
        cell = at(grid,pos)
        if cell.islower() and cell not in keys:
            yield dist, pos, keys | frozenset([cell])
        else:
            adjacent = [tuple(pos[i] + d[i] for i in range(2)) for d in deltas]
            for npos in [a for a in adjacent if a not in seen]:
                seen.add(npos)
                cell = at(grid,npos)
                if cell != wall and (not cell.isupper() or cell.lower() in keys):
                    todo.append((npos, dist + 1))

def run(data):
    grid = data.splitlines()
    xdim, ydim = len(grid[0]), len(grid)

    allkeys = set()
    for x,y in product(range(xdim),range(ydim)):
        cell = grid[y][x]
        if cell == entrance: start = (x,y)
        elif cell.islower(): allkeys.add(cell)

    todo = [(0, start, frozenset())]
    seen = set()
    while todo:
        dist, cpos, keys = heappop(todo)
        if keys == allkeys: 
            return dist

        if (cpos, keys) in seen: 
            continue

        seen.add((cpos, keys))
        for ndist, npos, nkeys in reachable_keys(cpos, keys, grid):    
            heappush(todo, (dist + ndist, npos, nkeys))

for test in [t for t in tests if t]:
    try: print(run(test))
    except Exception as e: 
        print(f"Error: {e}")

print(run(data))
