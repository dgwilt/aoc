def runphase(code,phase):
    namps = len(phase)
    amps = [Intcode() for _ in range(namps)]
    for i,a in enumerate(amps):
        a.program(code).provide_input(phase[i])
    amps[0].provide_input(0)

    for i,a in enumerate(amps):
        wire = a.execute().pop_outputs()
        amps[(i+1)%namps].provide_input(*wire)
    return wire[0]

def run(data,testing=False):
    if testing: return runphase(data[1],data[0])
    else: return max(runphase(data[1],phase) for phase in permutations(range(5),5))
    
