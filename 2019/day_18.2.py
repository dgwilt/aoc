entrance = "@"
wall = "#"
deltas = ( (-1, 0), (1, 0), (0, -1), (0, 1) )

def at(grid,pos):
    x,y = pos
    return grid[y][x]

def reachable_keys(start, keys, grid):
    todo = deque([(start, 0)])
    seen = set()
    while todo:
        pos, dist = todo.popleft()
        cell = at(grid,pos)
        if cell.islower() and cell not in keys:
            yield dist, pos, keys | frozenset([cell])
        else:
            adjacent = [tuple(pos[i] + d[i] for i in range(2)) for d in deltas]
            for npos in [a for a in adjacent if a not in seen]:
                seen.add(npos)
                cell = at(grid,npos)
                if cell != wall and (not cell.isupper() or cell.lower() in keys):
                    todo.append((npos, dist + 1))

def run(data):
    grid = data.splitlines()
    xdim, ydim = len(grid[0]), len(grid)

    allkeys = set()
    for x,y in product(range(xdim),range(ydim)):
        cell = grid[y][x]
        if cell == entrance:
            mid = x,y
            start = (
                (x-1, y-1),
                (x+1, y-1),
                (x-1, y+1),
                (x+1, y+1),
                )
        elif cell.islower(): allkeys.add(cell)

    x,y = mid
    grid[y-1] = grid[y-1][:x]   +  '#'  + grid[y-1][x+1:]
    grid[  y] = grid[y  ][:x-1] + '###' + grid[y  ][x+2:]
    grid[y+1] = grid[y+1][:x]   +  '#'  + grid[y+1][x+1:]

    todo = [(0, start, frozenset())]
    seen = [set() for _ in range(len(start))]
    while todo:
        dist, cpos, keys = heappop(todo)
        if keys == allkeys:
            return dist

        for i, pos in enumerate(cpos):
            if (pos, keys) in seen[i]:
                continue
            seen[i].add((pos, keys))
            for ndist, npos, nkeys in reachable_keys(pos, keys, grid):
                heappush(todo, (
                    dist + ndist, 
                    (*cpos[:i], npos, *cpos[i+1:]), 
                    nkeys
                    ))

print(run(data))
